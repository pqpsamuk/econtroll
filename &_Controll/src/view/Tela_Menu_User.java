    private void jEncerrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jEncerrarActionPerformed
        // encerra toda a sessao
        TempoSessao t = new TempoSessao();
        TempoSessaoDao dao = new TempoSessaoDao();
        t.setData_entrada(temposessao.getData_entrada());
        t.setData_saida(jData.getText());
        t.setDuracao(jTempo.getText());
        t.setId_usuario(jValor.getText());
        dao.adicionarSessao(t);
        if (cronometro.isRunning()) {
            cronometro.stop();
            jIniciar.setEnabled(true);
        }
        jIniciar.setText("Iniciar Sessão");
        jPause.setEnabled(false);
        jEncerrar.setEnabled(false);
        hora = 0;
        minutos = 0;
        segundos = 0;
        cronometro();
        EnviarEmail email = new EnviarEmail();
        email.enviar(t);
    }//GEN-LAST:event_jEncerrarActionPerformed

