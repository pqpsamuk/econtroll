    private Timer cronometro;
    private int hora, minutos, segundos;
    private int contador;
    private ActionListener acao = new ActionListener() {

        @Override
        public void actionPerformed(ActionEvent ae) {
            ++contador;
            if (contador == 100) {
                contador = 0;
                ++segundos;
            }
            if (segundos == 60) {
                segundos = 0;
                ++minutos;
            }
            if (minutos == 60) {
                minutos = 0;
                ++hora;
            }
            cronometro();
        }

    };

    private void cronometro() {
        String tempo = (hora <= 9 ? "0" : "") + hora + ":" + (minutos <= 9 ? "0" : "") + minutos + ":" + (segundos <= 9 ? "0" : "") + segundos;
        jTempo.setText(tempo);
    }

    public void hora() {
        ScheduledExecutorService scheduler = Executors.newScheduledThreadPool(1);

        scheduler.scheduleAtFixedRate(
                new Runnable() {
            public void run() {
                jData.setText(new SimpleDateFormat("HH:mm:ss  ||  dd/MM/yyyy").format(new Date()));
            }
        }, 1, 1, TimeUnit.SECONDS);
    }

