    private void jAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jAtualizarActionPerformed

        if (jfuncionario.getSelectedRow() != -1) {

            Usuario p = new Usuario();
            UsuarioDao dao = new UsuarioDao();
            
            p.setNome(jTextNome.getText());
            p.setCpf(jTextCPF.getText());
            p.setMatricula(jTextMatricula.getText());
            p.setSenha(jTextSenha.getText());
            p.setId((String) jfuncionario.getValueAt(jfuncionario.getSelectedRow(), 0));
            dao.Atualiza(p);

            jTextNome.setText("");
            jTextCPF.setText("");
            jTextMatricula.setText("");
            jTextSenha.setText("");

            MostraTabela();

        }

    
    }//GEN-LAST:event_jAtualizarActionPerformed

