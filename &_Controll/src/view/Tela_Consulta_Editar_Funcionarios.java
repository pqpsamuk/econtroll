    public void MostraTabela() {

        DefaultTableModel modelo = (DefaultTableModel) jfuncionario.getModel();
        UsuarioDao pdao = new UsuarioDao();
        modelo.setNumRows(0);
        for (Usuario p : pdao.listarUsuario()) {

            modelo.addRow(new Object[]{
                p.getId(),
                p.getNome(),
                p.getCpf(),
                p.getMatricula(),
                p.getSenha()
            });

        }

    }

    public void ListaDetalheTabela(String nome) {

        DefaultTableModel modelo = (DefaultTableModel) jfuncionario.getModel();
        modelo.setNumRows(0);
        UsuarioDao pdao = new UsuarioDao();

        for (Usuario p : pdao.ListaDetalhe(nome)) {

            modelo.addRow(new Object[]{
                p.getId(),
                p.getNome(),
                p.getCpf(),
                p.getMatricula(),
                p.getSenha()
            });

        }

    }

